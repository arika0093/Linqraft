using Linqraft.Core.Formatting;
using static Linqraft.Core.Generation.CodeTemplateContents;

namespace Linqraft.SourceGenerator;

internal static class SupportSourceEmitter
{
    public static string CreateSupportSource()
    {
        var builder = new IndentedStringBuilder();
        var extensionDeclarations =
            ProjectionSupportExtensionClassGenerator.CreateAllDeclarations();
        builder.AppendLines(
            $$"""
            {{AutoGeneratedHeader}}
            #nullable enable
            #pragma warning disable CS1591
            using System.Collections.Generic;
            using System.Linq;

            namespace Microsoft.CodeAnalysis
            {
                [global::System.AttributeUsage(global::System.AttributeTargets.All)]
                internal sealed partial class EmbeddedAttribute : global::System.Attribute
                {
                }
            }

            namespace System.Runtime.CompilerServices
            {
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                [global::System.AttributeUsage(global::System.AttributeTargets.Method, AllowMultiple = true, Inherited = false)]
                internal sealed partial class InterceptsLocationAttribute : global::System.Attribute
                {
                    public InterceptsLocationAttribute(int version, string data)
                    {
                        Version = version;
                        Data = data;
                    }

                    public int Version { get; }

                    public string Data { get; }
                }
            }

            namespace Linqraft
            {
                /// <summary>
                /// Defines how a Linqraft extension method transforms the expression when encountered inside a SelectExpr projection.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                internal enum LinqraftExtensionBehavior
                {
                    /// <summary>
                    /// Strips the extension call from the expression chain; the receiver expression is passed through unchanged.
                    /// </summary>
                    PassThrough = 0,

                    /// <summary>
                    /// Strips the extension call and makes the preceding navigation property access null-conditional,
                    /// which causes EF Core to generate a LEFT JOIN instead of an INNER JOIN.
                    /// </summary>
                    NullConditionalNavigation = 1,

                    /// <summary>
                    /// Strips the extension call and casts the receiver to the first generic type argument.
                    /// </summary>
                    CastToFirstTypeArgument = 2,
                }

                /// <summary>
                /// Marks a class as a Linqraft extension that can be used within SelectExpr projections.
                /// The source generator will generate a stub method and intercept its usage during projection generation.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
                internal sealed partial class LinqraftExtensionAttribute : global::System.Attribute
                {
                    public LinqraftExtensionAttribute(string methodName)
                    {
                        MethodName = methodName;
                    }

                    /// <summary>
                    /// The name of the extension method to generate and intercept.
                    /// </summary>
                    public string MethodName { get; }

                    /// <summary>
                    /// The namespace in which the stub extension method will be generated.
                    /// If null or empty, the stub will be placed in the <c>Linqraft</c> namespace.
                    /// </summary>
                    public string? GenerateNamespace { get; init; }

                    /// <summary>
                    /// Defines how the extension method transforms the expression in the generated projection.
                    /// </summary>
                    public LinqraftExtensionBehavior Behavior { get; init; }

                    /// <summary>
                    /// When <c>true</c>, indicates that the stub extension method is already provided
                    /// by the library or package that declares this extension class.
                    /// The Linqraft source generator will skip generating a duplicate stub.
                    /// </summary>
                    public bool StubAlreadyProvided { get; init; }
                }

                /// <summary>
                /// Base class for Linqraft extension implementations.
                /// Inherit from this class and apply <see cref="LinqraftExtensionAttribute"/> to define a custom SelectExpr extension.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                internal abstract partial class LinqraftExtensionBase { }

                /// <summary>
                /// Controls the accessibility of generated mapping helpers.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                internal enum LinqraftMappingVisibility
                {
                    Default = 0,
                    Internal = 1,
                    Public = 2,
                }

                /// <summary>
                /// Marks a mapping declaration that Linqraft should turn into a reusable projection method.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                [global::System.AttributeUsage(global::System.AttributeTargets.Class | global::System.AttributeTargets.Method, AllowMultiple = false, Inherited = false)]
                internal sealed partial class LinqraftMappingGenerateAttribute : global::System.Attribute
                {
                    public LinqraftMappingGenerateAttribute()
                    {
                    }

                    public LinqraftMappingGenerateAttribute(string methodName)
                    {
                        MethodName = methodName;
                    }

                    public string? MethodName { get; }

                    public LinqraftMappingVisibility Visibility { get; set; }
                }

                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                [global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
                internal sealed partial class LinqraftAutoGeneratedDtoAttribute : global::System.Attribute
                {
                }

                /// <summary>
                /// Base type for reusable mapping declarations. Override <see cref="DefineMapping"/> and describe the projection with <c>SelectExpr</c>.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                internal abstract partial class LinqraftMappingDeclare<T>
                {
                    protected IQueryable<T> Source => throw new global::System.InvalidOperationException("Linqraft source generator should replace mapping declarations before execution.");

                    protected abstract void DefineMapping();
                }

                /// <summary>
                /// Provides the object-conversion entry point that Linqraft intercepts before execution.
                /// </summary>
                [global::Microsoft.CodeAnalysis.EmbeddedAttribute]
                internal static partial class LinqraftKit
                {
                    private static global::System.InvalidOperationException ThrowInterceptionRequired
                        => new global::System.InvalidOperationException("Linqraft source generator should replace Generate invocations before execution.");

                    /// <summary>
                    /// Generates a DTO of type <typeparamref name="T"/> by evaluating the provided anonymous object initializer.
                    /// The initializer can contain any number of projections using <c>SelectExpr</c> and other supported Linqraft features,
                    /// and Linqraft will combine them into a single generated DTO type.
                    /// </summary>
                    public static T Generate<T>(object x)
                        => throw ThrowInterceptionRequired;

                    /// <summary>
                    /// Generates a DTO of type <typeparamref name="T"/> by evaluating the provided anonymous object initializer
                    /// together with anonymous-object capture data.
                    /// </summary>
                    [global::System.Obsolete("Anonymous-object capture is obsolete. Use the delegate-based capture pattern instead.", false)]
                    public static T Generate<T>(object x, object capture)
                        => throw ThrowInterceptionRequired;

                    /// <summary>
                    /// Generates a DTO of type <typeparamref name="T"/> by evaluating the provided anonymous object initializer
                    /// together with NativeAOT-safe capture data returned from <paramref name="capture"/>.
                    /// </summary>
                    public static T Generate<T>(object x, global::System.Func<object> capture)
                        => throw ThrowInterceptionRequired;
                }
            }

            """
        );
        builder.AppendLine("namespace Linqraft");
        builder.AppendLine("{");
        using (builder.Indent())
        {
            builder.AppendLines(extensionDeclarations);
        }
        builder.AppendLine("}");

        return builder.ToString();
    }
}
