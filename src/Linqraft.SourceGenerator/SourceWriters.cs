using System.Collections.Generic;
using System.Linq;
using Linqraft.Core.Configuration;
using Linqraft.Core.Formatting;
using Linqraft.Core.Generation;
using Linqraft.Core.Utilities;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace Linqraft.SourceGenerator;

internal static class SourceWriters
{
    public static string WriteDtoSource(GeneratedDtoModel dto, LinqraftConfiguration configuration)
    {
        var builder = CreateFileBuilder();

        WriteNamespaceAndContainingTypesStart(builder, dto.Namespace, dto.ContainingTypes);

        if (dto.IsAutoGeneratedNested)
        {
            builder.AppendLines(
                $$"""
                {{CodeTemplateContents.EditorBrowsableNeverAttribute}}
                [global::Linqraft.LinqraftAutoGeneratedDtoAttribute]
                """
            );
        }

        WriteDocumentation(builder, dto.Documentation, configuration.CommentOutput);

        var declarationKeyword = dto.IsRecord ? "record" : "class";
        var suppressedProperties = dto.Properties.Where(property => property.IsSuppressed).ToList();
        builder.AppendLines(
            $$"""
            {{dto.AccessibilityKeyword}} partial {{declarationKeyword}} {{dto.Name}}
            {
            """
        );
        using (builder.Indent())
        {
            if (suppressedProperties.Count != 0)
            {
                builder.AppendLine(
                    $"{dto.AccessibilityKeyword} {dto.Name}({string.Join(", ", suppressedProperties.Select(property => $"{property.TypeName} {ToParameterName(property.Name)}"))})"
                );
                builder.AppendLine("{");
                using (builder.Indent())
                {
                    foreach (var property in suppressedProperties)
                    {
                        builder.AppendLine($"{property.Name} = {ToParameterName(property.Name)};");
                    }
                }

                builder.AppendLine("}");
                builder.AppendLine();
            }

            foreach (var property in dto.Properties.Where(property => !property.IsSuppressed))
            {
                WriteDocumentation(builder, property.Documentation, configuration.CommentOutput);
                var required = configuration.HasRequired ? "required " : string.Empty;
                builder.AppendLine(
                    $"public {required}{property.TypeName} {property.Name} {{ {GetAccessor(configuration, dto.IsRecord)} }}"
                );
            }
        }

        builder.AppendLine("}");

        WriteNamespaceAndContainingTypesEnd(builder, dto.Namespace, dto.ContainingTypes);
        return builder.ToString();
    }

    public static string WriteInterceptorSource(
        ProjectionRequest request,
        SemanticModel semanticModel,
        LinqraftConfiguration configuration
    )
    {
        var builder = CreateFileBuilder();
        builder.AppendLines(
            """
            namespace Linqraft
            {
            """
        );
        using (builder.Indent())
        {
            builder.AppendLines(
                """
                file static partial class GeneratedExpression
                {
                """
            );
            using (builder.Indent())
            {
                var lambda = WriteProjectionLambda(request.RootProjection, request, semanticModel);
                var expressionFieldName = $"s_expression_{request.MethodName}";
                if (request.CanUsePrebuiltExpression && request.ReceiverKind == ReceiverKind.IQueryable)
                {
                    builder.AppendLine(
                        $"private static readonly global::System.Linq.Expressions.Expression<global::System.Func<{request.SourceTypeName}, {request.ResultTypeName}>> {expressionFieldName} = {lambda};"
                    );
                    builder.AppendLine();
                }

                var receiverType = request.ReceiverKind == ReceiverKind.IQueryable
                    ? "global::System.Linq.IQueryable"
                    : "global::System.Collections.Generic.IEnumerable";
                var selectorResultType = request.UseObjectSelectorSignature ? "object" : "TResult";
                var signature = request.Captures.Count == 0
                    ? $"public static {receiverType}<TResult> {request.MethodName}<TIn, TResult>(this {receiverType}<TIn> query, global::System.Func<TIn, {selectorResultType}> selector)"
                    : $"public static {receiverType}<TResult> {request.MethodName}<TIn, TResult>(this {receiverType}<TIn> query, global::System.Func<TIn, {selectorResultType}> selector, object capture)";
                if (request.InterceptableLocation is not null)
                {
                    builder.AppendLines(
                        $$"""
                        [global::System.Runtime.CompilerServices.InterceptsLocationAttribute({{request.InterceptableLocation.Version}}, "{{EscapeStringLiteral(request.InterceptableLocation.Data)}}")]
                        {{signature}}
                        {
                        """
                    );
                }
                else
                {
                    builder.AppendLines(
                        $$"""
                        {{signature}}
                        {
                        """
                    );
                }
                using (builder.Indent())
                {
                    builder.AppendLine(
                        $"var matchedQuery = query as {receiverType}<{request.SourceTypeName}> ?? throw new global::System.InvalidOperationException(\"Linqraft intercepted a SelectExpr call with an unexpected source type.\");"
                    );
                    foreach (var capture in request.Captures)
                    {
                        builder.AppendLine(
                            $"var {capture.LocalName} = global::Linqraft.LinqraftCaptureHelper.GetRequired<{capture.TypeName}>(capture, \"{EscapeStringLiteral(capture.PropertyName)}\");"
                        );
                    }

                    if (request.CanUsePrebuiltExpression && request.ReceiverKind == ReceiverKind.IQueryable)
                    {
                        builder.AppendLine($"var converted = matchedQuery.Select({expressionFieldName});");
                        builder.AppendLine(
                            $"return converted as {receiverType}<TResult> ?? throw new global::System.InvalidOperationException(\"Linqraft intercepted a SelectExpr call with an unexpected result type.\");"
                        );
                    }
                    else
                    {
                        builder.AppendLine($"var converted = matchedQuery.Select({lambda});");
                        builder.AppendLine(
                            $"return converted as {receiverType}<TResult> ?? throw new global::System.InvalidOperationException(\"Linqraft intercepted a SelectExpr call with an unexpected result type.\");"
                        );
                    }
                }

                builder.AppendLine("}");
            }

            builder.AppendLine("}");
        }

        builder.AppendLine("}");
        return builder.ToString();
    }

    public static string WriteMappingSource(
        MappingRequest request,
        SemanticModel semanticModel,
        LinqraftConfiguration configuration
    )
    {
        var builder = CreateFileBuilder();
        if (!string.IsNullOrWhiteSpace(request.Namespace))
        {
            builder.AppendLines(
                $$"""
                namespace {{request.Namespace}}
                {
                """
            );
            builder.IncreaseIndent();
        }

        builder.AppendLines(
            $$"""
            {{request.AccessibilityKeyword}} static partial class {{request.ContainingTypeName}}
            {
            """
        );
        using (builder.Indent())
        {
            var receiverType = request.ReceiverKind == ReceiverKind.IQueryable
                ? "global::System.Linq.IQueryable"
                : "global::System.Collections.Generic.IEnumerable";
            var lambda = WriteProjectionLambda(request.RootProjection, request, semanticModel);
            var expressionFieldName = $"s_expression_{request.MethodName}";
            if (request.CanUsePrebuiltExpression && request.ReceiverKind == ReceiverKind.IQueryable)
            {
                builder.AppendLine(
                    $"private static readonly global::System.Linq.Expressions.Expression<global::System.Func<{request.SourceTypeName}, {request.ResultTypeName}>> {expressionFieldName} = {lambda};"
                );
                builder.AppendLine();
            }

            builder.AppendLines(
                $$"""
                {{request.AccessibilityKeyword}} static {{receiverType}}<{{request.ResultTypeName}}> {{request.MethodName}}(this {{receiverType}}<{{request.SourceTypeName}}> source)
                {
                """
            );
            using (builder.Indent())
            {
                if (request.CanUsePrebuiltExpression && request.ReceiverKind == ReceiverKind.IQueryable)
                {
                    builder.AppendLine($"return source.Select({expressionFieldName});");
                }
                else
                {
                    builder.AppendLine($"return source.Select({lambda});");
                }
            }

            builder.AppendLine("}");
        }

        builder.AppendLine("}");

        if (!string.IsNullOrWhiteSpace(request.Namespace))
        {
            builder.DecreaseIndent();
            builder.AppendLine("}");
        }

        return builder.ToString();
    }

    private static string WriteProjectionLambda(
        ProjectionObjectModel projection,
        ProjectionRequest request,
        SemanticModel semanticModel
    )
    {
        return $"{request.SelectorParameterName} => {WriteProjectionBody(projection, request.Pattern, request.ResultTypeName, semanticModel)}";
    }

    private static string WriteProjectionLambda(
        ProjectionObjectModel projection,
        MappingRequest request,
        SemanticModel semanticModel
    )
    {
        return $"{request.SelectorParameterName} => {WriteProjectionBody(projection, ProjectionPattern.ExplicitDto, request.ResultTypeName, semanticModel)}";
    }

    private static string WriteProjectionBody(
        ProjectionObjectModel projection,
        ProjectionPattern pattern,
        string resultTypeName,
        SemanticModel semanticModel
    )
    {
        var kind = pattern == ProjectionPattern.Anonymous ? "anonymous" : "named";
        var targetType = pattern == ProjectionPattern.Anonymous ? null : resultTypeName;
        var constructorArguments = projection.Members
            .Where(member => member.IsSuppressed)
            .Select(
                member =>
                {
                    var emitter = new ProjectionExpressionEmitter(
                        semanticModel,
                        member.Expression,
                        member.TypeName,
                        member.UseEmptyCollectionFallback,
                        member.ReplacementTypes
                    );
                    return emitter.Emit(member.Expression);
                }
            )
            .ToList();
        var assignments = projection.Members
            .Where(member => !member.IsSuppressed)
            .Select(
            member =>
            {
                var emitter = new ProjectionExpressionEmitter(
                    semanticModel,
                    member.Expression,
                    member.TypeName,
                    member.UseEmptyCollectionFallback,
                    member.ReplacementTypes
                );
                return $"{member.Name} = {emitter.Emit(member.Expression)}";
            }
        ).ToList();

        return kind == "anonymous"
            ? $"new {{ {string.Join(", ", assignments)} }}"
            : WriteNamedProjection(targetType!, constructorArguments, assignments);
    }

    private static IndentedStringBuilder CreateFileBuilder()
    {
        var builder = new IndentedStringBuilder();
        builder.AppendLines(
            $$"""
            {{CodeTemplateContents.AutoGeneratedHeader}}
            #nullable enable
            #pragma warning disable CS1591
            #pragma warning disable CS8600
            #pragma warning disable CS8601
            #pragma warning disable CS8602
            #pragma warning disable CS8603
            #pragma warning disable CS8604
            #pragma warning disable CS8618
            """
        );
        return builder;
    }

    private static string GetAccessor(LinqraftConfiguration configuration, bool isRecord)
    {
        return configuration.PropertyAccessor switch
        {
            LinqraftPropertyAccessor.GetAndSet => "get; set;",
            LinqraftPropertyAccessor.GetAndInit => "get; init;",
            LinqraftPropertyAccessor.GetAndInternalSet => "get; internal set;",
            _ => isRecord ? "get; init;" : "get; set;",
        };
    }

    private static void WriteDocumentation(
        IndentedStringBuilder builder,
        Linqraft.Core.Documentation.DocumentationInfo? documentation,
        LinqraftCommentOutput outputMode
    )
    {
        if (documentation is null || outputMode == LinqraftCommentOutput.None)
        {
            return;
        }

        if (!string.IsNullOrWhiteSpace(documentation.Summary))
        {
            var summary = documentation.Summary!;
            builder.AppendLine("/// <summary>");
            foreach (var line in summary.Split('\n'))
            {
                builder.AppendLine($"/// {line.Trim()}");
            }

            builder.AppendLine("/// </summary>");
        }

        if (outputMode == LinqraftCommentOutput.All && !string.IsNullOrWhiteSpace(documentation.Remarks))
        {
            var remarks = documentation.Remarks!;
            builder.AppendLine("/// <remarks>");
            foreach (var line in remarks.Split('\n'))
            {
                builder.AppendLine($"/// {line.Trim()}");
            }

            builder.AppendLine("/// </remarks>");
        }
    }

    private static void WriteNamespaceAndContainingTypesStart(
        IndentedStringBuilder builder,
        string namespaceName,
        IReadOnlyList<ContainingTypeInfo> containingTypes
    )
    {
        if (!string.IsNullOrWhiteSpace(namespaceName))
        {
            builder.AppendLines(
                $$"""
                namespace {{namespaceName}}
                {
                """
            );
            builder.IncreaseIndent();
        }

        foreach (var containingType in containingTypes)
        {
            builder.AppendLines(
                $$"""
                {{containingType.AccessibilityKeyword}} partial {{containingType.DeclarationKeyword}} {{containingType.Name}}
                {
                """
            );
            builder.IncreaseIndent();
        }
    }

    private static void WriteNamespaceAndContainingTypesEnd(
        IndentedStringBuilder builder,
        string namespaceName,
        IReadOnlyList<ContainingTypeInfo> containingTypes
    )
    {
        for (var index = containingTypes.Count - 1; index >= 0; index--)
        {
            builder.DecreaseIndent();
            builder.AppendLine("}");
        }

        if (!string.IsNullOrWhiteSpace(namespaceName))
        {
            builder.DecreaseIndent();
            builder.AppendLine("}");
        }
    }

    private static string EscapeStringLiteral(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    private static string WriteNamedProjection(
        string targetType,
        IReadOnlyList<string> constructorArguments,
        IReadOnlyList<string> assignments
    )
    {
        var constructorSuffix = constructorArguments.Count == 0
            ? "()"
            : $"({string.Join(", ", constructorArguments)})";
        if (assignments.Count == 0)
        {
            return $"new {targetType}{constructorSuffix}";
        }

        return $"new {targetType}{constructorSuffix} {{ {string.Join(", ", assignments)} }}";
    }

    private static string ToParameterName(string propertyName)
    {
        return string.IsNullOrEmpty(propertyName)
            ? "value"
            : char.ToLowerInvariant(propertyName[0]) + propertyName[1..];
    }
}
